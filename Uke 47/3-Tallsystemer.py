#%% Desimal til binær
desimal_tall = 73
print(bin(desimal_tall))

#%% Binær til desimal
binar_tall = '1001101'
desimal_tegn = int(binar_tall, 2)
print((int(binar_tall, 2)))
print(desimal_tegn)

#%% desimal til hex (16 tallsystemet)
des_tall = 77
hex_tall = hex(des_tall)
print(hex_tall)

#%% hex til desimal
hex_tegn = '0xff'
des_tegn = int(hex_tegn, 16)
print(des_tegn)
